package JavaPractice;

public class Narrow_Casting {

	public static void main(String[] args) {
		 // bigger  data type
        double doubleEx = 15.7;


        // Narrow casting double to integer  
        int intEx = (int) doubleEx;


        //printing double value
        System.out.println(" double value: " + doubleEx);


        //printing integer value
        System.out.println(" Narrowed integer value: " + intEx);
		
	}

}
/*
double value: 15.7
Narrowed integer value: 15
*/
